		temp4=(temp&0x10)>>1;
		temp5=(temp&0x08)<<4;
		temp6=(temp&0x04)<<1;
		temp7=(temp&0x02)<<6;
		temp8=(temp&0x01)<<3;
		h11=temp1|temp1>>1|temp1>>2|temp1>>3;
		h12=temp2|temp2>>1|temp2>>2|temp2>>3;
		h13=temp3|temp3>>1|temp3>>2|temp3>>3;
		h14=temp4|temp4>>1|temp4>>2|temp4>>3;
		h15=temp5|temp5>>1|temp5>>2|temp5>>3;
