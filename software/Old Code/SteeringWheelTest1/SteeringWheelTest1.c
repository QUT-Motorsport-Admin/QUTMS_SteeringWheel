	if(dat & 128) PORTB |= 0b00001000;
	else PORTB &= ~ 0b00001000;
	
	if(dat & 64) PORTC |= 0b01000000;
	else PORTC &= ~ 0b01000000;
	
	if(dat & 32) PORTC |= 0b00100000;
	else PORTC &= ~ 0b00100000;
	
	if(dat & 16) PORTB |= 0b00000100;
	else PORTB &= ~ 0b00000100;
	
	if(dat & 8) PORTD |= 0b10000000;
	else PORTD &= ~ 0b10000000;
	
	if(dat & 4) PORTB |= 0b00000010;
	else PORTB &= ~ 0b00000010;
	
	if(dat & 2) PORTB |= 0b00000001;
	else PORTB &= ~ 0b00000001;
	
	if(dat & 1) PORTB |= 0b10000000;
	else PORTB &= ~ 0b10000000;
	
	PORTC |= 0b10000000;	//set Write Pin High
	PORTD |= 0b00000001;	//Set CS Pin High
	
	return;
	
